def run_talosctl_genconfig(spec: TalosClusterSpec, output_dir: str) -> bool:
    """Optionally call talosctl to generate secrets and merge with our structural configs."""
    if not spec.nodes:
        return False
    try:
        result = subprocess.run(
            ["talosctl", "gen", "config", spec.cluster_name, spec.cluster_endpoint,
             "--output-dir", output_dir, "--output-types", "controlplane,worker,talosconfig",
             "--force"],
            capture_output=True, text=True, timeout=30,
        )
        if result.returncode == 0:
            print("[talos-config] talosctl genconfig succeeded — secrets embedded in config files")
            return True
        else:
            print(f"[talos-config] talosctl genconfig failed: {result.stderr.strip()}")
            return False
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return False


