def generate_node_patch(node: TalosNodeSpec, output_dir: str) -> None:
    """Generate a per-node strategic merge patch for network and hostname."""
    patch: dict = {
        "machine": {
            "network": {
                "hostname": node.hostname,
                "interfaces": [
                    {
                        "interface": "eth0",
                        "addresses": [f"{node.ip}/24"],
                        "routes": [
                            {
                                "network": "0.0.0.0/0",
                                "gateway": node.gateway,
                            }
                        ],
                        "dhcp": False,
                    }
                ],
                "nameservers": [node.nameserver],
            }
        }
    }

    patches_dir = os.path.join(output_dir, "patches")
    os.makedirs(patches_dir, exist_ok=True)
    path = os.path.join(patches_dir, f"{node.vm_name}.yaml")
    with open(path, "w") as f:
        f.write(f"# Per-node patch for {node.vm_name} ({node.role})\n")
        f.write(f"# Apply with: talosctl apply-config --nodes {node.ip} \\\n")
        f.write(f"#   --file {node.role}.yaml --patch @patches/{node.vm_name}.yaml\n\n")
        f.write(_emit_yaml(patch))
        f.write("\n")
    print(f"[talos-config] Written: {path}")


