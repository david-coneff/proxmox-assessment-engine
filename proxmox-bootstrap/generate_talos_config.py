# ── Loaders ────────────────────────────────────────────────────────────────────

def _load_yaml_simple(path: str) -> dict:
    """Minimal YAML loader for the project's config files (stdlib only).

    Handles nested mappings, sequences, comments, and quoted strings.
    Not a full YAML parser — sufficient for k3s-cluster.yaml and bootstrap-state.json.
    """
    try:
        import yaml  # type: ignore
        with open(path) as f:
            return yaml.safe_load(f) or {}
    except ImportError:
        pass

    # Fallback: line-by-line parser for simple YAML
    with open(path) as f:
        lines = f.readlines()

    def _parse(lines: list[str], base_indent: int = 0) -> dict | list | str | None:
        result: dict | list | None = None
        i = 0
        while i < len(lines):
            raw = lines[i]
            stripped = raw.rstrip()
            if not stripped or stripped.lstrip().startswith('#'):
                i += 1
                continue
            indent = len(raw) - len(raw.lstrip())
            if indent < base_indent:
                break
            content = stripped.strip()
            if content.startswith('- '):
                if result is None:
                    result = []
                assert isinstance(result, list)
                val = content[2:].strip()
                if ':' in val:
                    sub = {val.split(':', 1)[0].strip(): val.split(':', 1)[1].strip() or None}
                    result.append(sub)
                else:
                    result.append(_clean_val(val))
            elif ':' in content:
                if result is None:
                    result = {}
                assert isinstance(result, dict)
                k, _, v = content.partition(':')
                k = k.strip()
                v = v.strip()
                # Comment strip
                if ' #' in v:
                    v = v[:v.index(' #')].strip()
                if v == '':
                    # Block value follows
                    child_lines = []
                    j = i + 1
                    while j < len(lines):
                        child_raw = lines[j]
                        child_stripped = child_raw.rstrip()
                        if not child_stripped or child_stripped.lstrip().startswith('#'):
                            j += 1
                            child_lines.append(child_raw)
                            continue
                        child_indent = len(child_raw) - len(child_raw.lstrip())
                        if child_indent <= indent:
                            break
                        child_lines.append(child_raw)
                        j += 1
                    result[k] = _parse(child_lines, indent + 1)
                    i = j
                    continue
                else:
                    result[k] = _clean_val(v)
            i += 1
        return result

    def _clean_val(v: str):
        if v in ('true', 'True'):
            return True
        if v in ('false', 'False'):
            return False
        if v in ('null', 'Null', 'None', '~', ''):
            return None
        try:
            return int(v)
        except ValueError:
            pass
        try:
            return float(v)
        except ValueError:
            pass
        # Strip quotes
        if (v.startswith('"') and v.endswith('"')) or (v.startswith("'") and v.endswith("'")):
            return v[1:-1]
        return v

    return _parse(lines) or {}


def _find_vm_ip(state: dict, vm_name: str) -> str:
    """Look up VM IP from dns_registry or vms list."""
    for entry in state.get("dns_registry", []):
        if entry.get("hostname") == vm_name or entry.get("name") == vm_name:
            return entry.get("ip", "POPULATE")
    for vm in state.get("vms", []):
        if vm.get("name") == vm_name:
            return vm.get("ip", "POPULATE")
    return "POPULATE"


def _gateway(state: dict) -> str:
    topo = state.get("network_topology_declared", {}) or state.get("network_topology", {})
    return (topo.get("gateway") or
            state.get("host_identity", {}).get("gateway") or
            "POPULATE")


def _nameserver(state: dict) -> str:
    hi = state.get("host_identity", {}) or {}
    topo = state.get("network_topology_declared", {}) or {}
    return (hi.get("nameserver") or
            topo.get("dns_servers", [None])[0] if topo.get("dns_servers") else None or
            hi.get("lan_ip") or
            "POPULATE")


def build_cluster_spec(k3s: dict, state: dict) -> TalosClusterSpec:
    """Build a TalosClusterSpec from k3s-cluster.yaml and bootstrap-state.json."""
    cluster_name = k3s.get("cluster_name", "homelab-k3s")
    hi = state.get("host_identity", {}) or {}
    gateway = _gateway(state)
    nameserver = _nameserver(state)

    nodes: list[TalosNodeSpec] = []

    for node in k3s.get("server_nodes", []) or []:
        if node.get("os_variant", "ubuntu") != "talos":
            continue
        vm_name = node.get("vm_name", "k3s-server-01")
        ip = _find_vm_ip(state, vm_name)
        nodes.append(TalosNodeSpec(
            vm_name=vm_name,
            role="controlplane",
            hostname=vm_name,
            ip=ip,
            gateway=gateway,
            nameserver=nameserver,
        ))

    for node in k3s.get("worker_nodes", []) or []:
        if node.get("os_variant", "ubuntu") != "talos":
            continue
        vm_name = node.get("vm_name", "k3s-worker-01")
        ip = _find_vm_ip(state, vm_name)
        nodes.append(TalosNodeSpec(
            vm_name=vm_name,
            role="worker",
            hostname=vm_name,
            ip=ip,
            gateway=gateway,
            nameserver=nameserver,
        ))

    # Cluster endpoint = first control-plane node IP
    cp_nodes = [n for n in nodes if n.role == "controlplane"]
    if cp_nodes and cp_nodes[0].ip != "POPULATE":
        endpoint = f"https://{cp_nodes[0].ip}:6443"
    else:
        endpoint = "https://POPULATE:6443"

    k3s_version = k3s.get("kubernetes_version", None)
    if isinstance(k3s_version, str) and k3s_version.startswith("POPULATE"):
        k3s_version = None

    return TalosClusterSpec(
        cluster_name=cluster_name,
        cluster_endpoint=endpoint,
        nodes=nodes,
        k3s_version=k3s_version,
    )


