# ── YAML emit (stdlib only) ────────────────────────────────────────────────────
# We only need a small subset of YAML; implement a minimal emitter.

def _yaml_str(v: str) -> str:
    """Quote a string value if it contains special YAML characters."""
    need_quote = any(c in v for c in (':', '#', '[', ']', '{', '}', ',', '&', '*', '?', '|', '<', '>', '=', '!', '%', '@', '`', "'", '"', '\n'))
    if need_quote:
        escaped = v.replace('\\', '\\\\').replace('"', '\\"')
        return f'"{escaped}"'
    return v


def _yaml_val(v) -> str:
    if v is None:
        return "null"
    if isinstance(v, bool):
        return "true" if v else "false"
    if isinstance(v, (int, float)):
        return str(v)
    return _yaml_str(str(v))


def _emit_yaml(obj, indent: int = 0) -> str:
    pad = "  " * indent
    if isinstance(obj, dict):
        lines = []
        for k, v in obj.items():
            if isinstance(v, (dict, list)):
                lines.append(f"{pad}{k}:")
                lines.append(_emit_yaml(v, indent + 1))
            else:
                lines.append(f"{pad}{k}: {_yaml_val(v)}")
        return "\n".join(lines)
    if isinstance(obj, list):
        if not obj:
            return f"{pad}[]"
        lines = []
        for item in obj:
            if isinstance(item, (dict, list)):
                first_line, *rest_lines = _emit_yaml(item, indent + 1).splitlines()
                lines.append(f"{pad}- {first_line.lstrip()}")
                for rl in rest_lines:
                    lines.append(rl)
            else:
                lines.append(f"{pad}- {_yaml_val(item)}")
        return "\n".join(lines)
    return f"{pad}{_yaml_val(obj)}"


