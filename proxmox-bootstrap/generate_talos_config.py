# ── Main ───────────────────────────────────────────────────────────────────────

def generate_talos_configs(
    state_path: str,
    k3s_path: str,
    output_dir: str,
    genconfig: bool = False,
) -> TalosClusterSpec:
    """Full config generation pipeline. Returns the cluster spec."""
    state = json.load(open(state_path)) if os.path.exists(state_path) else {}
    k3s = _load_yaml_simple(k3s_path) if os.path.exists(k3s_path) else {}

    spec = build_cluster_spec(k3s, state)

    talos_nodes = [n for n in spec.nodes if n.os_variant == "talos"]
    if not talos_nodes:
        print("[talos-config] No nodes declared with os_variant: talos in k3s-cluster.yaml")
        print("[talos-config] Set os_variant: talos on a server_nodes or worker_nodes entry to activate.")

    os.makedirs(output_dir, exist_ok=True)

    generate_installer_template(output_dir)

    used_talosctl = False
    if genconfig:
        used_talosctl = run_talosctl_genconfig(spec, output_dir)
        if not used_talosctl:
            print("[talos-config] talosctl not available — generating structural stubs only")

    if not used_talosctl:
        generate_base_controlplane(spec, output_dir)
        generate_base_worker(spec, output_dir)
        generate_talosconfig_stub(spec, output_dir)

    for node in talos_nodes:
        generate_node_patch(node, output_dir)

    write_readme(spec, output_dir, used_talosctl)

    return spec


