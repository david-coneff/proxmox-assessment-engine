    parser = argparse.ArgumentParser(
        description="Generate Talos machine configs from k3s-cluster.yaml and bootstrap-state.json"
    )
    parser.add_argument("--state",     default="proxmox-bootstrap/bootstrap-state.json",
                        help="Path to bootstrap-state.json")
    parser.add_argument("--k3s",       default="proxmox-bootstrap/metadata/k3s-cluster.yaml",
                        help="Path to k3s-cluster.yaml")
    parser.add_argument("--output",    default="talos-configs",
                        help="Output directory for generated configs")
    parser.add_argument("--genconfig", action="store_true",
                        help="Call talosctl gen config to fill secrets (requires talosctl installed)")
    args = parser.parse_args(argv)

    spec = generate_talos_configs(
        state_path=args.state,
        k3s_path=args.k3s,
        output_dir=args.output,
        genconfig=args.genconfig,
    )

    talos_count = sum(1 for n in spec.nodes if n.os_variant == "talos")
    print(f"[talos-config] Done. {talos_count} Talos node(s) configured in {args.output}/")
    return 0


