"""
generate_talos_config.py — Talos machine config generator (stdlib only)

Reads k3s-cluster.yaml and bootstrap-state.json to generate declarative Talos
machine configs for each node declared with os_variant: talos.

Output: talos-configs/
  controlplane.yaml      — base control-plane machine config
  worker.yaml            — base worker machine config
  patches/<vm_name>.yaml — per-node IP/hostname patches
  installer-template.yaml — minimal config for template build (no cluster join)
  talosconfig            — operator client config (fill endpoint/CA after genconfig)

Usage:
  python3 proxmox-bootstrap/generate_talos_config.py \\
      --state  proxmox-bootstrap/bootstrap-state.json \\
      --k3s    proxmox-bootstrap/metadata/k3s-cluster.yaml \\
      --output talos-configs/

  python3 proxmox-bootstrap/generate_talos_config.py --help

Design notes:
  - All output is plain YAML; no external deps required.
  - Cluster secret (token, CA) must be generated by talosctl genconfig first;
    this tool generates the structural/network patches that override those secrets.
  - If talosctl is available, --genconfig flag calls it and merges output.
  - Nodes with os_variant != 'talos' are silently skipped.
  - An installer-template.yaml is always generated for use by build-talos-template.sh.
"""

